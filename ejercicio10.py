'''Escribe un programa que determine el día de la semana correspondiente a un
número ingresado por el usuario (1 para lunes, 2 para martes, etc.)'''


n = int(input('introduzca un numero del 1 al 7:'))

if n == 1:
       print ('lunes')
elif n == 2:
      print ('martes')
elif n == 3:
     print ('miercoles')
elif n == 4:
     print ('jueves')
elif n == 5:
     print ('viernes')
elif n == 6:
         print ('sabado')
elif n == 7:
     print ('domingo')
    






       


    
